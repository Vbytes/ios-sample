- (void)viewDidLoad
{
    [super viewDidLoad];
    
    self.view.autoresizesSubviews = YES;
    [self.view addSubview:self.mediaControl];
    // loading view
    self.activityIndicator = [[UIActivityIndicatorView alloc] initWithFrame:self.view.bounds];
    self.activityIndicator.autoresizingMask = UIViewAutoresizingFlexibleWidth|UIViewAutoresizingFlexibleHeight;
    [self.view addSubview:self.activityIndicator];
    [self.activityIndicator startAnimating];
    
    // Do any additional setup after loading the view from its nib.

//    [[UIApplication sharedApplication] setStatusBarHidden:YES];
//    [[UIApplication sharedApplication] setStatusBarOrientation:UIInterfaceOrientationLandscapeLeft animated:NO];

#ifdef DEBUG
    [IJKFFMoviePlayerController checkIfFFmpegVersionMatch:YES];
    // [IJKFFMoviePlayerController checkIfPlayerVersionMatch:YES major:1 minor:0 micro:0];
    [IJKFFMoviePlayerController setLogReport:YES];
    [IJKFFMoviePlayerController setLogLevel:k_IJK_LOG_DEBUG];
#else
    [IJKFFMoviePlayerController setLogReport:NO];
    [IJKFFMoviePlayerController setLogLevel:k_IJK_LOG_INFO];
#endif

    if (self.isLive) {
        [LiveController load:[self.url absoluteString] resolution:@"UHD" listener:^(NSURL *proxyUrl) {
            self.url = proxyUrl;
            [self launchPlayer];
        }];
    } else {
        // [self launchPlayer];
        [VodController load:[self.url absoluteString] resolution:@"UHD" listener:^(NSURL *proxyUrl) {
            self.url = proxyUrl;
            [self launchPlayer];
        }];
    }
}

- (void)launchPlayer {
    IJKFFOptions *options = [IJKFFOptions optionsByDefault];
    // [options setFormatOptionValue:@"ijktcphook" forKey:@"http-tcp-hook"];
    [options setCodecOptionValue:@"skip_loop_filter" forKey:@"48"];
    [options setFormatOptionValue:@"probesize" forKey:@"4096"];
    [options setFormatOptionValue:@"anayzeduration" forKey:@"500000"];
    [options setPlayerOptionValue:@"packet-buffing" forKey:@"0"];
    
    self.player = [[IJKFFMoviePlayerController alloc] initWithContentURL:self.url withOptions:options];
    self.player.view.autoresizingMask = UIViewAutoresizingFlexibleWidth|UIViewAutoresizingFlexibleHeight;
    self.player.view.frame = self.view.bounds;
    self.player.scalingMode = IJKMPMovieScalingModeAspectFit;
    self.player.shouldAutoplay = YES;
    
    [self.activityIndicator stopAnimating];
    [self.activityIndicator removeFromSuperview];
    [self.mediaControl removeFromSuperview];
    
    [self.view addSubview:self.player.view];
    [self.view addSubview:self.mediaControl];
    
    self.mediaControl.delegatePlayer = self.player;
    
    [self installMovieNotificationObservers];
    
    [self.player prepareToPlay];
}

